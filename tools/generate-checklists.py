def main() -> None:
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)
    plans = [
        ("dsar-log.xlsx", build_dsar_log()),
        ("breach-triage.xlsx", build_breach_triage()),
        ("vendor-register.xlsx", build_vendor_register()),
    ]
    for filename, workbook in plans:
        standardise(workbook)
        output_path = OUTPUT_DIR / filename
        workbook.save(output_path)
        print(f"wrote {output_path.relative_to(OUTPUT_DIR.parents[2])}")


if __name__ == "__main__":
    main()
